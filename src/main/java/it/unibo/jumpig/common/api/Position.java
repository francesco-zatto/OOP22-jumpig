package it.unibo.jumpig.common.api;

/**
 * An interface that represents the position in a Cartesian Plane.
 */
public interface Position {
    /**
     * Getter for the x, the coordinate in the horizontal axis.
     * @return the coordinate in the x axis.
     */
    double getX();

    /**
     * Getter for the y, the coordinate in the horizontal axis.
     * @return the coordinate in the y axis.
     */
    double getY();
}
